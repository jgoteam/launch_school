The issue here is that another `Employee` instance assigned to `other` is trying to access the private `serial_number` getter
method defined on line 66.
Private methods are only available within the class definition, aka only to `self``.
What we need is a protected method, which is available within the class definition, but also to instances of the same class, as long as this call to this instance is WITHIN the same class definition.